// ─────────────────────────────────────────────────────────────────────────────

func TestGenerate_ScopeFieldsPropagated(t *testing.T) {
	gen, _ := NewDefaultRuleGenerator()

	recipient := "0x5B38Da6a701c568545dCfcB03FcB875f56beddC4"
	req := &types.SignRequest{
		ID:            "req-1",
		ChainType:     types.ChainTypeEVM,
		ChainID:       "137",
		APIKeyID:      "api-key-42",
		SignerAddress: "0xf39Fd6e51aad88F6F4ce6aB8827279cffFb92266",
	}
	parsed := &types.ParsedPayload{Recipient: &recipient}
	opts := &RuleGenerateOptions{
		RuleType: types.RuleTypeEVMAddressList,
		RuleMode: types.RuleModeWhitelist,
	}

	rule, err := gen.Generate(req, parsed, opts)
	require.NoError(t, err)
	require.NotNil(t, rule.ChainType)
	assert.Equal(t, types.ChainTypeEVM, *rule.ChainType)
	require.NotNil(t, rule.ChainID)
	assert.Equal(t, "137", *rule.ChainID)
	require.NotNil(t, rule.APIKeyID)
	assert.Equal(t, "api-key-42", *rule.APIKeyID)
	require.NotNil(t, rule.SignerAddress)
	assert.Equal(t, "0xf39Fd6e51aad88F6F4ce6aB8827279cffFb92266", *rule.SignerAddress)
}
