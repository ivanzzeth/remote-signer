// Generate: address list
// ─────────────────────────────────────────────────────────────────────────────

func TestGenerate_AddressList_Whitelist(t *testing.T) {
	gen, _ := NewDefaultRuleGenerator()

	recipient := "0x5B38Da6a701c568545dCfcB03FcB875f56beddC4"
	req := &types.SignRequest{
		ID:            "req-1",
		ChainType:     types.ChainTypeEVM,
		ChainID:       "1",
		APIKeyID:      "key-1",
		SignerAddress: "0xf39Fd6e51aad88F6F4ce6aB8827279cffFb92266",
	}
	parsed := &types.ParsedPayload{Recipient: &recipient}
	opts := &RuleGenerateOptions{
		RuleType: types.RuleTypeEVMAddressList,
		RuleMode: types.RuleModeWhitelist,
	}

	rule, err := gen.Generate(req, parsed, opts)
	require.NoError(t, err)
	assert.Equal(t, types.RuleTypeEVMAddressList, rule.Type)
	assert.Equal(t, types.RuleModeWhitelist, rule.Mode)
	assert.Equal(t, types.RuleSourceAutoGenerated, rule.Source)
	assert.True(t, rule.Enabled)
	assert.Contains(t, rule.Name, "Allow:")
	assert.Contains(t, rule.Name, recipient)
	assert.NotEmpty(t, rule.Config)

	// Verify config
	var cfg addressListConfig
	require.NoError(t, json.Unmarshal(rule.Config, &cfg))
	assert.Equal(t, []string{recipient}, cfg.Addresses)

	// Rule ID is not prefixed with "preview_"
	assert.False(t, strings.HasPrefix(string(rule.ID), "preview_"))
}

