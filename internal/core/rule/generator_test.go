// ─────────────────────────────────────────────────────────────────────────────
// RuleGenerateOptions.Validate
// ─────────────────────────────────────────────────────────────────────────────

func TestRuleGenerateOptions_Validate(t *testing.T) {
	maxVal := "1000"

	tests := []struct {
		name    string
		opts    RuleGenerateOptions
		wantErr string
	}{
		{"missing rule_type", RuleGenerateOptions{RuleMode: types.RuleModeWhitelist}, "rule_type is required"},
		{"missing rule_mode", RuleGenerateOptions{RuleType: types.RuleTypeEVMAddressList}, "rule_mode is required"},
		{"invalid rule_mode", RuleGenerateOptions{RuleType: types.RuleTypeEVMAddressList, RuleMode: "invalid"}, "must be 'whitelist' or 'blocklist'"},
		{"unsupported rule_type", RuleGenerateOptions{RuleType: "evm_js", RuleMode: types.RuleModeWhitelist}, "unsupported rule_type"},
		{"address_list valid", RuleGenerateOptions{RuleType: types.RuleTypeEVMAddressList, RuleMode: types.RuleModeWhitelist}, ""},
		{"contract_method valid", RuleGenerateOptions{RuleType: types.RuleTypeEVMContractMethod, RuleMode: types.RuleModeWhitelist}, ""},
		{"value_limit missing max_value", RuleGenerateOptions{RuleType: types.RuleTypeEVMValueLimit, RuleMode: types.RuleModeWhitelist}, "max_value is required"},
		{"value_limit valid", RuleGenerateOptions{RuleType: types.RuleTypeEVMValueLimit, RuleMode: types.RuleModeWhitelist, MaxValue: &maxVal}, ""},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			err := tc.opts.Validate()
			if tc.wantErr != "" {
				assert.Error(t, err)
				assert.Contains(t, err.Error(), tc.wantErr)
			} else {
				assert.NoError(t, err)
			}
		})
	}
}

